class Literals {
    public static void main(String args[]){
        byte b1=10; //decimal
        byte b2=0b1010; //10 in binary
        byte b3=012; //10 in octal
        byte b4=0XA; //10 in hexadecimal
        System.out.println(b1);
        System.out.println(b2);
        System.out.println(b3);
        System.out.println(b4);
    }
}
